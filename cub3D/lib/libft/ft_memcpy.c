#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*dst_ptr;
	unsigned char	*src_ptr;

	dst_ptr = (unsigned char *)dst;
	src_ptr = (unsigned char *)src;
	if (dst == src)
		return (NULL);
	while (n > 0)
	{
		*dst_ptr++ = *src_ptr++;
		n--;
	}
	return (dst);
}
